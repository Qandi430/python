#리스트 자료형

odd = [1,3,5,7,9]
# 리스트를 만들때는 대괄호 [] 로 감싸주고 각 요소 값은 쉽표로 구분해 준다.

a = []
b = [1,2,3]
c = ['Life','is','too','short']
d = [1,2,'Life','is']
e = [1,2,['Life','is']]
# 리스트는 
# a처럼 비어있는 리스트 일 수도 있고
# b처럼 숫자를 요소값처럼 가질수도 있고
# c처럼 문자열을 요소값처럼 가질수도 있고
# d처럼 숫자와 문자열을 함께 요소값으로 가질수 있고
# e처럼 리스트를 요소값으로 가질수도 있다.

# 리스트의 인덱싱
a = [1,2,3]
# 0부터 순서대로 요소를 반환
print(a[0])
# 리스트 요소값으로 사칙연산
print(a[0]+a[2])
# 인덱스가 음수면 뒤에서부터 반환
print(a[-1])
# 다차원 배열 인덱싱
a = [1,2,3,['a','b','c',['Life',"is"]]]
print(a[-1][0])
print(a[-1][2])
print(a[-1][3][1])

# 리스트의 슬라이싱
# 문자열 슬리이싱과 동일
a = [1,2,3,4,5]
# 1번째 요소부터 2번째요소까지
print(a[0:2])
# 1번째 요소부터 2번째요소까지
print(a[:2])
# 2번째요소부터 마지막까지
print(a[2:])
# 다차원 리스트 인덱싱
a = [1,2,3,['a','b','c'],4,5]
print(a[2:5])
print(a[3][:2])


# 리스트 연산

# 리스트 더하기(+)
a = [1,2,3]
b = [4,5,6]
print(a+b)
# 리스트 반복하기(*)
a = [1,2,3]
print(a*3)
# 리스트 길이구하기
a = [1,2,3]
print(len(a))
